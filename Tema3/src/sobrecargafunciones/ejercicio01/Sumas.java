package sobrecargafunciones.ejercicio01;

public class Sumas {
	// creamos la primera funcion int que recive dos enteros y devuelve la suma de
	// los dos enteros
	public static int suma(int num1, int num2) {
		return num1 + num2;
	}
	
	// creamos la segunda funcion double que recive dos doubles y devuelve la suma de
	// los dos doubles
	public static double suma(double num1, double num2) {
		return num1 + num2;
	}

}
