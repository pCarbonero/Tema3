package sobrecargafunciones.ejercicio02;

public class Media {
	
	public static int media(int num1, int num2) {
		int res;
		res = (num1 + num2)/2;
		return res;
	}
	
	public static int media(int num1, int num2, int num3) {
		int res;
		res = (num1 + num2 + num3)/3;
		return res;
	}

}
